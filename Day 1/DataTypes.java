public class PrimitiveDataTypes{
    public static void main(String[] args) {
        
        System.out.println("The range for bytes is from " + Byte.MAX_VALUE + " to " + Byte.MIN_VALUE);
        System.out.println("The range for integers is from " + Integer.MIN_VALUE + " to " + Integer.MAX_VALUE);
        System.out.println("The range for long is from " + Long.MAX_VALUE + " to " + Long.MIN_VALUE);
        System.out.println("The range for short is from " + Short.MAX_VALUE + " to " + Short.MIN_VALUE);
        System.out.println("The range for boolean is from 0 to 1");
        System.out.println("The range for doubles is from " + Double.MAX_VALUE + " to " + Double.MIN_VALUE);
        System.out.println("The range for bytes is from " + 'a' + " to " + 'z');
    }
}
